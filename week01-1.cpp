#include <stdio.h> ///標準的input/output

int main()
{
	int a,b; ///整數a,b
	scanf("%d%d",&a,&b); /// 讀入資料(小心 "%d%d" 後面「逗號」)
	/// 兩個"%d%d" &a,&b
	printf("%d",a+b); /// 印出資料, 把資料印出來
}
